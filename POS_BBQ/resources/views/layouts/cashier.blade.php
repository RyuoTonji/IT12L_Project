        <script>
            document.addEventListener('DOMContentLoaded', function () {
                const logoutBtn = document.getElementById('logout-btn-cashier');
                const logoutForm = document.getElementById('logout-form-cashier');
                const modal = document.getElementById('report-reminder-modal');

                if (logoutBtn) {
                    logoutBtn.addEventListener('click', function (e) {
                        e.preventDefault();

                        fetch('{{ route('shift-report.check') }}')
                            .then(response => response.json())
                            .then(data => {
                                if (data.has_report) {
                                    logoutForm.submit();
                                } else {
                                    modal.classList.remove('hidden');
                                }
                            })
                            .catch(error => {
                                console.error('Error checking shift report:', error);
                                modal.classList.remove('hidden');
                            });
                    });
                }
            });
